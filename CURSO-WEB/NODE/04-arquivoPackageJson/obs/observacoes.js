// VERSÕES

//As versões são descritas por um numero de decimal

//  Ex: 0.17.1 
// 0 (Primeiro algarismo) é o valor Major
// 17 (Segundo algarismo) é o valor Minor
// 1 (Terceiro algarismo) é o valor fixed

/* As vezes você encontrara a versão com o simbolo "^" ou "~" 
antes do primeiro algarismo, EX: ^0.17.1 ou ~0.17.1.

Quando há um "^" significa que só o valor minor pode ser 
alterada.

Quando houver "~" significa que só o valor fized pode ser alterado .

Caso não haja nem um simbolo o valor major pode ser alterado.
*/