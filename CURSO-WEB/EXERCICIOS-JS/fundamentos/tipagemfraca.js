// JavaScript é uma linguagem fracamente tipada
// As variaveis não necessitam a declaração do tipo de dado como inteiro, boleano ou decimal

/* Por conta de ser uma linguagem fracamente tipada pode-se declarar uma variavel e traibuir 
com tipo de valor string e em seguida atribuir um outro tipo de valor com numerico por exemplo*/
let qualquer = 'lugar'
console.log(qualquer)
console.log(typeof qualquer)

qualquer = 3.14337
console.log(qualquer)
console.log(typeof qualquer)
